    private void botaoRemoverConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverConsultaActionPerformed
        Consulta consulta = tabelaConsultas.obterSelecionado();
        if (consulta != null) {
            sistema.removerConsulta(consulta.getId());
            JOptionPane.showMessageDialog(
                this, 
                "Consulta removida do banco de dados com sucesso", 
                "Consulta removida", 
                JOptionPane.INFORMATION_MESSAGE
            );
            realizarBusca();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma consulta antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_botaoRemoverConsultaActionPerformed

