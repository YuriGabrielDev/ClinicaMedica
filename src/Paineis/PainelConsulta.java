    private void botaoEditarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarPacienteActionPerformed
        PainelClinicaMedica painel = (PainelClinicaMedica) getParent();
        Consulta consulta = tabelaConsultas.obterSelecionado();
        if (consulta != null) {
            painel.getPainelEdicaoConsulta().setIdConsulta(consulta.getId());
            painel.mostrarPainel("EditarConsulta");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma consulta antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoEditarPacienteActionPerformed

