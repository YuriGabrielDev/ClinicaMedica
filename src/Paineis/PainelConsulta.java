    private void realizarBusca() {
        List<Consulta> consultas = sistema.pesquisarConsultas(
            campoPaciente.getText(),
            campoMedico.getText(),
            campoTipo.getText()
        );
        tabelaConsultas.setConsultas(consultas);
    }
    
    
