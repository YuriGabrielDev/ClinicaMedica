    private void botaoEditarProntuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarProntuarioActionPerformed
        PainelClinicaMedica painel = (PainelClinicaMedica) getParent();
        Prontuario prontuario = tabelaProntuarios.obterSelecionado();
        if (prontuario != null) {
            if (Objects.equals(prontuario.getMedico().getId(), idMedico)) {
                painel.getPainelEdicaoProntuario().setIdProntuario(prontuario.getId());
                painel.mostrarPainel("EditarProntuario");
            } else {
                JOptionPane.showMessageDialog(
                    this, 
                    "Só é permitido editar prontuários que você cadastrou", 
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma prontuario antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoEditarProntuarioActionPerformed

