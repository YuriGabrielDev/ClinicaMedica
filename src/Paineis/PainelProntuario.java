    private void botaoGerarDeclaracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGerarDeclaracaoActionPerformed
        Prontuario prontuario = tabelaProntuarios.obterSelecionado();
        if (prontuario != null) {
            String nomeAcompanhante = JOptionPane.showInputDialog(
                this,
                "Informe o nome do acompanhante:"
            );
            String declaracao =
                "Declaro para os devidos fins que " + nomeAcompanhante +
                " acompanhou o(a) paciente " +
                prontuario.getPaciente().getNome() +
                " em consulta médica.\n" +
                "Médico: " + prontuario.getMedico().getNome() + "\n" +
                "Data: " + prontuario.getDataCriacao() + "\n";

            JOptionPane.showMessageDialog(
                this,
                declaracao,
                "Declaração de acompanhamento",
                JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma prontuario antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoGerarDeclaracaoActionPerformed

