    private void botaoRemoverProntuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverProntuarioActionPerformed
        Prontuario prontuario = tabelaProntuarios.obterSelecionado();
        if (prontuario != null) {
            if (Objects.equals(prontuario.getMedico().getId(), idMedico)) {
                sistema.removerProntuario(prontuario.getId());
                realizarBusca();
                JOptionPane.showMessageDialog(
                    this, 
                    "Prontuário removido do banco de dados com sucesso", 
                    "Prontuário removido", 
                    JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                    this, 
                    "Só é permitido editar prontuários que você cadastrou", 
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma prontuario antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_botaoRemoverProntuarioActionPerformed

