    private void botaoEditarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarPacienteActionPerformed
        PainelClinicaMedica painel = (PainelClinicaMedica) getParent();
        Paciente paciente = tabelaPacientes.obterSelecionado();
        if (paciente != null) {
            painel.getPainelEdicaoPaciente().setIdPaciente(paciente.getId());
            painel.mostrarPainel("EditarPaciente");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um paciente antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoEditarPacienteActionPerformed

