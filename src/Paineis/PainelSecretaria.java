    private void botaoRemoverPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverPacienteActionPerformed
        Paciente paciente = tabelaPacientes.obterSelecionado();
        if (paciente != null) {
            sistema.removerPaciente(paciente.getId());
            JOptionPane.showMessageDialog(
                this, 
                "Paciente removido do banco de dados com sucesso", 
                "Paciente removido", 
                JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um paciente antes", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        realizarBusca();
    }//GEN-LAST:event_botaoRemoverPacienteActionPerformed

