    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        Boolean correto = true;
        String conteudoAviso = null;
        String cpf = "";
        String senha = "";
        
        if (campoTextoCPF.getText().length() == 0) {
            correto = false;
            conteudoAviso = "CPF não foi preenchido";
        } else {
            cpf = campoTextoCPF.getText();
        }
        
        if (campoSenha.getPassword().length == 0) {
            correto = false;
            conteudoAviso = "senha não foi preenchida";
        } else {
            senha = new String(campoSenha.getPassword());
        }
        
        if (correto) {
           Medico dadosMedico = sistema.obterMedico(cpf);
           if (dadosMedico != null) {
               if (senha.equals(dadosMedico.getSenha())) {
                   entrarMedico(dadosMedico);
                   limpar();
               } else {
                   conteudoAviso = "Senha incorreta";
               }
           } else {
               Secretaria dadosSecretaria = sistema.obterSecretaria(cpf);
               if (dadosSecretaria != null) {
                   if (senha.equals(dadosSecretaria.getSenha())) {
                       entrarSecretaria(dadosSecretaria);
                       limpar();
                    } else {
                       conteudoAviso = "Senha incorreta";
                    }
               } else {
                   conteudoAviso = "Esse cpf não está cadastrado";
               }
           }
       } 
       if (conteudoAviso != null) {
           JOptionPane.showMessageDialog(this, conteudoAviso, "Aviso", JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_botaoEntrarActionPerformed

