    private void entrarMedico(Medico dadosMedicos) {
        PainelClinicaMedica painel = (PainelClinicaMedica) getParent();
        painel.getPainelMedico().setIdMedico(dadosMedicos.getId());
        painel.mostrarPainel("Medico");
    }
    
    private void entrarSecretaria(Secretaria dadosSecretaria) {
        PainelClinicaMedica painel = (PainelClinicaMedica) this.getParent();
        painel.getPainelSecretaria().setIdSecretaria(dadosSecretaria.getId());
        painel.mostrarPainel("Secretaria");
    }
    
