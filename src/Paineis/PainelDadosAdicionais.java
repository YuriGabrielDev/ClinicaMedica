    /**
     * Atualiza as areas de texto da interface
     */
    private void atualizarAreas() {
        Paciente paciente = sistema.obterPaciente(idPaciente);
        areaTextoElementosPrejudiciais.setText(paciente.getComportamentosPrejudiciais());
        areaTextoComorbidades.setText(paciente.getComorbidades());
        areaTextoCirurgias.setText(paciente.getCirurgias());
        areaTextoAlergias.setText(paciente.getAlergias());
    }
    
